package dataPackage;

public class UserInputs {
	private boolean acc_Status;
	private float setSpeed;
	private float setDistance;
	public boolean isAcc_Status() {
		return acc_Status;
	}
	public void setAcc_Status(boolean acc_status) {
		this.acc_Status = acc_status;
	}
	public float getSetSpeed() {
		return setSpeed;
	}
	public void setSetSpeed(float setSpeed) {
		this.setSpeed = setSpeed;
	}
	public float getSetDistance() {
		return setDistance;
	}
	public void setSetDistance(float setDistance) {
		this.setDistance = setDistance;
	} 
	
	
}
